//  Math In JavaScript
//  Methods
//Math.round()
let num = 123.56;
console.log(Math.round(num)); //124
let num1 =123.456;  
console.log(Math.round(num1)); //123 


//Math.floor()

console.log(Math.floor(1));  //1
console.log(Math.floor(1.5)); //1
console.log(Math.floor(1.4));  //1
console.log(Math.floor(1.9));  //1
console.log(Math.floor(-1.9));  //-2

//Math.ceil()
console.log(Math.ceil(1));  //1
console.log(Math.ceil(1.5)); //2
console.log(Math.ceil(1.4));  //2
console.log(Math.ceil(1.9));  //2
console.log(Math.ceil(-1.9));  //-1

 
//Math.abs()

console.log(Math.abs(1));   //1 
console.log(Math.abs(-1));  //1
console.log(Math.abs(2));   //2
console.log(Math.abs(-2));  //2

//Math.sqrt()
console.log(Math.sqrt(9)); //3
console.log(Math.sqrt(2)); //1.4142135623730951
console.log(Math.sqrt(4)); //2


//Math.pow()

console.log(Math.pow(2,6)); //2**6 // 64


//Math.min()

console.log(Math.min(2,4,567,8,3,2,1,46,89,35,78)); //1

//Math.max()

console.log(Math.max(2,4,567,8,3,2,1,46,89,35,78)); //567

//Math.random()
console.log(Math.random());  //gives value 0 --> 1 and 1 not include
